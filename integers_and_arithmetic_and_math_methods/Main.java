package integers_and_arithmetic_and_math_methods;

public class Main {
    public static void main(String[] args) {
        /* Integers, Math, and Arithmetic Operations */
        int x = 100;
        int y = 20;
        int result;

        // addition
        result = x + y;
        System.out.println(result);

        // subtraction
        result = x - y;
        System.out.println(result);

        // multiplication
        result = x * y;
        System.out.println(result);

        // division
        result = x / y;
        System.out.println(result);

        // remainder
        result = 20 % 2;
        System.out.println(result);

        // MATH METHODS
        System.out.println(Math.max(x, y));
        System.out.println(Math.abs(-500));
    }
}
