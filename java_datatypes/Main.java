package java_datatypes;

public class Main {
    public static void main(String[] args) {
        /* Data Types */

        /* Numerical Types */
        // Mainly used
        int aNumber = 42;   // Integer: [-2,147,483,648, 2,147,483,647]
        float aFloatingNumber = 13.37f;     // Floating point number: [7 decimal point precision]
        double aDoubleNumber = 420.0005;    // Double number: [15 decimal point precision]
        
        // Sometimes used
        byte aTinyNumber = 100;     // One byte (or 8 bits) stores numbers: [-128, 127]
        short aSmallNumber = 30000;     // short stores numbers from [-32,768 to 32,767]
        long aLongNumber = 50000000;    // Long [-9,223,372,036,854,775,808, 9,223,372,036,854,775,807]

        /* Booleans (Logic) */
        Boolean greatVideos = true;
        Boolean isThisHTML = false;

        /* Characters & Strings */
        char aSingleCharacter = 'j';
        String message = "Hello World";
        String test = null;
    }
}